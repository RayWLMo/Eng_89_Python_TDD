class SimpleCalc:
    def add(self, num1, num2):
        return num1 + num2  # If the outcome is 5, the condition is True and the test would pass

    def subtract(self, num1, num2):
        return num1 - num2

    def multiply(self, num1, num2):
        return num1 * num2

    def divide(self, num1, num2):
        return num1 / num2
